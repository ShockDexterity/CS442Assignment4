int main()
{
	int size {0};
	char* commandString[size + 1];
	while (true)
	{
		printf("Hello %s\n", "World!");
		printf("Amogus\n");

		string command;
		getline(cin, command);

		if (command == "exit")
		{
			break;
		}

		int org_STDIN { dup(0) };
		int org_STDOUT { dup(1) };

		int fdChildToParent[2] {}; // [0] is reading
		int fdParentToChild[2] {}; // [1] is writing

		if (pipe(fdChildToParent) < 0)
		{
			cout << "Failed to make fdChildToParent into a pipe" << '\n';
		}
		if (pipe(fdParentToChild) < 0)
		{
			cout << "Failed to make fdParentToChild into a pipe" << '\n';
		}

		pid_t pid { fork() };

		if (pid < 0)
		{
			cout << "Failed to make a child." << '\n';
			exit(1);
		}
		else if (pid == 0)
		{
			// Child process

			close(fdParentToChild[1]);
			read(fdParentToChild[0], (char**)commandString, sizeof(commandString));
			close(fdParentToChild[0]);

			print(commandString, sizeof(commandString));

			if (contains((char*)commandString, (char*)"pwd"))
			{
				char path[_PC_PATH_MAX];
				if (getcwd(path, sizeof(path)) != NULL)
				{
					cout << path << endl;
				}
			}

			exit(0);
		}
		else
		{
			// Parent process
			// cout << amogus;
			//int size {0};
			vector<string> commandTokens {getCommand(size)};
			//char* commandString[size + 1];
			commandString[size] = NULL;

			//Filling the c_string array for execvp use
			fill(commandString, commandTokens);

			close(fdParentToChild[0]);
			write(fdParentToChild[1], (char**)commandString, sizeof(commandString));
			close(fdParentToChild[1]);


		}
	}

	return 0;
}



